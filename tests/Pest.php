<?php

use Tests\TestCase;

/*
|--------------------------------------------------------------------------
| Test Case
|--------------------------------------------------------------------------
|
| همه Feature tests از Laravel TestCase ارث می‌برن تا به
| application container، database، و HTTP helpers دسترسی داشته باشن.
|
*/

pest()->extend(TestCase::class)
    ->in('Feature');

/*
|--------------------------------------------------------------------------
| Module Test Directories
|--------------------------------------------------------------------------
|
| تست‌های داخل هر ماژول هم از همین TestCase استفاده می‌کنن.
| وقتی ماژول جدیدی اضافه شد، مسیر Tests/Feature آن را اینجا اضافه کن.
|
*/

pest()->extend(TestCase::class)
    ->in('../Modules');
